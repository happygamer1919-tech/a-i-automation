// ============ Navbar Scroll ============
const navbar = document.getElementById('navbar');

window.addEventListener('scroll', () => {
    navbar.classList.toggle('scrolled', window.scrollY > 50);
});

// ============ Mobile Menu ============
const hamburger = document.getElementById('hamburger');
const navLinks = document.getElementById('navLinks');

hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('active');
    navLinks.classList.toggle('open');
});

// Close menu on link click
navLinks.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
        hamburger.classList.remove('active');
        navLinks.classList.remove('open');
    });
});

// ============ Scroll Reveal ============
const revealElements = document.querySelectorAll('.reveal');

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            observer.unobserve(entry.target);
        }
    });
}, {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
});

revealElements.forEach(el => observer.observe(el));

// ============ Form Handling ============
const form = document.getElementById('contactForm');

form.addEventListener('submit', (e) => {
    // If no Formspree ID is set, fall back to mailto
    const action = form.getAttribute('action');
    if (action.includes('your-form-id')) {
        e.preventDefault();
        const name = document.getElementById('name').value;
        const phone = document.getElementById('phone').value;
        const company = document.getElementById('company').value;
        const description = document.getElementById('description').value;

        const subject = encodeURIComponent(`Audit Request from ${name} - ${company}`);
        const body = encodeURIComponent(
            `Name: ${name}\nPhone: ${phone}\nCompany: ${company}\n\nDescription:\n${description}`
        );

        window.location.href = `mailto:info@a-iautomation.com?subject=${subject}&body=${body}`;
    }
});

// ============ Smooth Scroll for CTA ============
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', (e) => {
        e.preventDefault();
        const target = document.querySelector(anchor.getAttribute('href'));
        if (target) {
            const offset = 80;
            const top = target.getBoundingClientRect().top + window.scrollY - offset;
            window.scrollTo({ top, behavior: 'smooth' });
        }
    });
});
