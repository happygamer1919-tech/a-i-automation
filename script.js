// ============ Language Switcher ============
let currentLang = localStorage.getItem('lang') || 'ro';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update meta tags
    const metaDesc = document.querySelector('meta[name="description"]');
    const ogTitle = document.querySelector('meta[property="og:title"]');
    const ogDesc = document.querySelector('meta[property="og:description"]');
    const title = document.querySelector('title');

    if (lang === 'ru') {
        title.textContent = 'A&I Automation — AI-автоматизация для бизнеса в Молдове';
        metaDesc.content = 'A&I Automation помогает бизнесу в Молдове устранить рутинную работу с помощью AI-инструментов, чат-ботов и интеллектуальных агентов.';
        ogTitle.content = 'A&I Automation — AI-автоматизация для бизнеса в Молдове';
        ogDesc.content = 'Автоматизируем рутинную работу в вашем бизнесе с помощью AI-инструментов, чат-ботов и интеллектуальных агентов.';
    } else if (lang === 'en') {
        title.textContent = 'A&I Automation — AI Automation for Businesses in Moldova';
        metaDesc.content = 'A&I Automation helps businesses in Moldova eliminate repetitive work with AI tools, chatbots, and intelligent agents.';
        ogTitle.content = 'A&I Automation — AI Automation for Businesses in Moldova';
        ogDesc.content = 'We automate the repetitive work in your business with AI tools, chatbots, and intelligent agents.';
    } else {
        title.textContent = 'A&I Automation — Automatizare AI pentru afaceri din Moldova';
        metaDesc.content = 'A&I Automation ajută afacerile din Moldova să elimine munca repetitivă cu instrumente AI, chatboți și agenți inteligenți.';
        ogTitle.content = 'A&I Automation — Automatizare AI pentru afaceri din Moldova';
        ogDesc.content = 'Automatizăm munca repetitivă din afacerea ta cu instrumente AI, chatboți și agenți inteligenți.';
    }
}

// Init language buttons
document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
});

